char	*ft_rot42(char *str)
{
	char *i;

	i = str;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			*str = (((*str - 'a' + 42) % 26) + 'a');
		if (*str >= 'A' && *str <= 'Z')
			*str = (((*str - 'A' + 42) % 26) + 'A');
		str++;
	}
	return (i);
}

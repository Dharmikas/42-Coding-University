int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	else
	{
		while (sqrt < nb)
		{
			if (sqrt * sqrt == nb)
				return (sqrt);
			sqrt = sqrt + 1;
		}
	}
	return (0);
}
